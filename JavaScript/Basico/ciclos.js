// Ciclo While
let contador = 0;

while (contador < 3) {
  console.log(contador);
  contador++;
}
contador -= 3;
console.log("Fin Ciclo While");

// Ciclo Do While
do {
  console.log(contador);
  contador++;
} while (contador < 3);
console.log("Fin Ciclo Do While");

// Ciclo For
for(contador =0;contador<3;contador++){
    console.log(contador);
}
console.log("Fin Ciclo For");
